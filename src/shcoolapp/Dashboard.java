    public void showUcenici() throws SQLException {
        service s = new service();
        List<Ucenik> ucenici = s.getUcenici();
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        
        for(int i = 0; i < ucenici.size(); i++) {
            Object[] row = new Object[3];
            row[0] = ucenici.get(i).getId();
            row[1] = ucenici.get(i).getIme();
            row[2] = ucenici.get(i).getPrezime();
            model.addRow(row);
        }
    }
    
